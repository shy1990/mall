package com.sanji.mall.accessory.special.model;

import java.math.BigDecimal;

/**
 * 
* @ClassName: AccessoryViewModel
* @Description: 配件视图模型
* @author WuJiming wzslw_163_com
* @date 2014年10月18日 上午9:39:48
 */
public class AccessoryViewModel {
	private String id;
	private String catName;
	private String brandName;
	private String name;
	private String description;
	private String imageSrc;
	private BigDecimal price;
	private BigDecimal originalPrice;
	private BigDecimal tmallPrice;
	private String tmallUrl;
	private Integer quantity;
	
	
	public String getTmallUrl() {
		return tmallUrl;
	}
	public void setTmallUrl(String tmallUrl) {
		this.tmallUrl = tmallUrl;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCatName() {
		return catName;
	}
	public void setCatName(String catName) {
		this.catName = catName;
	}
	public String getBrandName() {
		return brandName;
	}
	public void setBrandName(String brandName) {
		this.brandName = brandName;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getImageSrc() {
		return imageSrc;
	}
	public void setImageSrc(String imageSrc) {
		this.imageSrc = imageSrc;
	}
	public BigDecimal getPrice() {
		return price;
	}
	public void setPrice(BigDecimal price) {
		this.price = price;
	}
	public BigDecimal getOriginalPrice() {
		return originalPrice;
	}
	public void setOriginalPrice(BigDecimal originalPrice) {
		this.originalPrice = originalPrice;
	}
	public BigDecimal getTmallPrice() {
		return tmallPrice;
	}
	public void setTmallPrice(BigDecimal tmallPrice) {
		this.tmallPrice = tmallPrice;
	}
	public Integer getQuantity() {
		return quantity;
	}
	public void setQuantity(Integer quantity) {
		this.quantity = quantity;
	}
	
	
}
